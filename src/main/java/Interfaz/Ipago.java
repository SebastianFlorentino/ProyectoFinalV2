  private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    int cedula=0;
    String vlrCedula=txtcedula.getText();
    try {
      
        String sql="select cedula,nombre,p.id,p.precio from cliente as c join plan as p on c.idplan=p.id where c.id='"+vlrCedula+"'";
        Connection cn= con.EstablacerConnection();
        Statement mySta=cn.createStatement();
        ResultSet myRes=mySta.executeQuery(sql);
         
        while (myRes.next()) {  
          cedula=1;
          txtcedula.setEditable(false);
          txtnombre.setText(myRes.getString(2));
          txtidplan.setText(myRes.getString(3));
          txtprecio.setText(myRes.getString(4));
        
      }
        if (cedula==0) {
          JOptionPane.showMessageDialog(null, "No se ha encontrado el cliente");
      }
  
    } catch (Exception e) {
      JOptionPane.showMessageDialog(null, "Error:"+ e);

    }
   
  
  }//GEN-LAST:event_btnBuscarActionPerformed

  private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    String sql = "INSERT INTO pago (cedula,nombre,idplan,precio,monto,fechaPago) VALUES ( ?, ?, ?, ?, ?,CURRENT_DATE)";

    try {
      PreparedStatement pst = cn.prepareStatement(sql);
      pst.setString(1, txtcedula.getText());
      pst.setString(2, txtnombre.getText());
      pst.setString(3, txtidplan.getText());
      pst.setString(4, txtprecio.getText());
      pst.setString(5, txtmonto.getText());
      


      pst.executeUpdate();
      JOptionPane.showMessageDialog(null, "Pago realizado al dedillo");
      mostrarDatos();
      limpiarCampos();
  }//GEN-LAST:event_btnGuardarActionPerformed
  catch (Exception ex) {
      JOptionPane.showMessageDialog(null, "Error: "+ex);
    }
  }
  
